function approxHessian = initializeApproxHessian(samps, wName,... 
                                                 startLevel, stopLevel,...
                                                 coeffs, coeffsIdx,...
                                                 scalingOnly, sampleSupport)
approxHessian = 4*eye(size(coeffs,1));
